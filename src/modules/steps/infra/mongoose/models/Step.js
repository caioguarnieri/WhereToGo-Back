import mongoose from "mongoose";

const stepSchema = new mongoose.Schema({
  travelId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "Travel",
  },
  destination: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  budget: {
    type: Number,
    required: true,
  },
  startDate: {
    type: Date,
    required: true,
  },
  endDate: {
    type: Date,
    required: true,
  },
  status: {
    type: String,
    required: true,
  },
  latitude: {
    type: String,
    required: true,
  },
  longitude: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
  updatedAt: {
    type: Date,
    default: new Date(),
  },
});

const Step = mongoose.models.Step || mongoose.model("Step", stepSchema);
export default Step;
